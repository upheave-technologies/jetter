stack_sandbox_count_active() {
  local slug
  slug=$(stack_project_slug)
  python3 - "$STACK_REGISTRY" "$slug" <<'PY' 2>/dev/null
import json, sys
file, slug = sys.argv[1], sys.argv[2]
try:
    with open(file) as f: data = json.load(f)
except Exception:
    print(0); sys.exit(0)
n = sum(1 for i in data.get("instances", []) if i.get("kind") == "sandbox" and i.get("project") == slug)
print(n)
PY
}

stack_sandbox_parse_duration() {
  local s=$1
  if [[ "$s" =~ ^([0-9]+)$ ]]; then
    printf "%d" "${BASH_REMATCH[1]}"
  elif [[ "$s" =~ ^([0-9]+)s$ ]]; then
    printf "%d" "${BASH_REMATCH[1]}"
  elif [[ "$s" =~ ^([0-9]+)m$ ]]; then
    printf "%d" $(( ${BASH_REMATCH[1]} * 60 ))
  elif [[ "$s" =~ ^([0-9]+)h$ ]]; then
    printf "%d" $(( ${BASH_REMATCH[1]} * 3600 ))
  elif [[ "$s" =~ ^([0-9]+)d$ ]]; then
    printf "%d" $(( ${BASH_REMATCH[1]} * 86400 ))
  else
    return 1
  fi
}

stack_sandbox_iso_offset() {
  local secs=$1
  python3 -c "
from datetime import datetime, timezone, timedelta
print((datetime.now(timezone.utc) + timedelta(seconds=$secs)).isoformat().replace('+00:00', 'Z'))
"
}

stack_sandbox_register() {
  local id=$1 name=$2 cproj=$3 override=$4 ttl=$5 expires_at=$6
  local slug worktree path now
  slug=$(stack_project_slug)
  worktree=$(stack_current_worktree)
  path=$STACK_REPO_ROOT
  now=$(date -u +%Y-%m-%dT%H:%M:%SZ)

  python3 - <<PY
import json
file = "$STACK_REGISTRY"
entry = {
    "id": "$id",
    "kind": "sandbox",
    "name": "$name",
    "project": "$slug",
    "worktree": "$worktree",
    "path": "$path",
    "profile": "$(stack_default_profile)",
    "compose_project": "$cproj",
    "override_file": "$override",
    "ephemeral": True,
    "ports": {},
    "urls": [],
    "ttl_seconds": $ttl,
    "created_at": "$now",
    "expires_at": "$expires_at",
    "hooks_applied": False,
}
try:
    with open(file) as f: data = json.load(f)
except Exception:
    data = {"version": 1, "instances": []}
others = [i for i in data.get("instances", []) if i.get("id") != "$id"]
data["instances"] = others + [entry]
with open(file, "w") as f:
    json.dump(data, f, indent=2)
PY
}

stack_sandbox_mark_hooks_applied() {
  local id=$1
  python3 - "$STACK_REGISTRY" "$id" <<'PY' 2>/dev/null
import json, sys
file, target = sys.argv[1], sys.argv[2]
try:
    with open(file) as f: data = json.load(f)
except Exception:
    sys.exit(0)
for inst in data.get("instances", []):
    if inst.get("id") == target:
        inst["hooks_applied"] = True
with open(file, "w") as f:
    json.dump(data, f, indent=2)
PY
}

# Determine the image name compose would build for the PRIMARY stack's app
# service. Matches compose's default tagging: <project>-<service>.
stack_sandbox_primary_image_name() {
  local svc=$1
  local slug worktree primary_proj
  slug=$(stack_project_slug)
  worktree=$(stack_current_worktree)
  # Replicates stack_compose_project_name's transformation without the override.
  primary_proj=$(printf "%s@%s" "$slug" "$worktree" | tr '@' '_' | tr '#' '_' | tr '/' '-' | tr '[:upper:]' '[:lower:]')
  printf "%s-%s" "$primary_proj" "$svc"
}

